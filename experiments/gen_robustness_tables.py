def fig_order_grid_curves():
    """Universality shown as CONVERGENCE, not flat order points: terminal KL vs N for all 36 configs, at
    each config's own lambda* (steep N^-4 family) and beside it at 0.7 lambda* (shallow N^-2 family). Every
    config's lambda* curve plunges, every off-curve stays shallow -- the order jump is universal, and the
    curves' natural spread is the variability across (B,T,s^2). Exact extended-precision recursion."""
    r = L("e23_order_grid")
    if not r: return
    import numpy as np, matplotlib
    matplotlib.use("Agg"); import matplotlib.pyplot as plt
    from diffusion import vp_const, set_dps
    from recursion import v_terminal
    from metrics import kl_gauss
    set_dps(30)
    plt.rcParams.update({"font.size": 11, "savefig.dpi": 170, "axes.spines.top": False,
                         "axes.spines.right": False, "savefig.bbox": "tight"})
    Ns = [32, 64, 128, 256, 512, 1024, 2048]
    rows = r["rows"]
    s2vals = sorted(set(row["s2"] for row in rows))
    cmap = {s: c for s, c in zip(s2vals, plt.cm.viridis(np.linspace(0.12, 0.85, len(s2vals))))}
    fig, ax = plt.subplots(figsize=(7.2, 4.7))
    floor = 1e-19
    for row in rows:
        B, T, s2 = row["B"], row["T"], row["s2"]
        lam = float(row["lambda_star"]); sched = vp_const(B, s2, T)
        kstar = [max(float(kl_gauss(v_terminal(sched, N, lam ** 2), s2)), floor) for N in Ns]
        koff = [max(float(kl_gauss(v_terminal(sched, N, (0.7 * lam) ** 2), s2)), floor) for N in Ns]
        c = cmap[s2]
        ax.loglog(Ns, kstar, "-", color=c, lw=1.1, alpha=0.55, zorder=3)      # steep family (at lambda*)
        ax.loglog(Ns, koff, "-", color="0.6", lw=0.7, alpha=0.25, zorder=2)   # shallow family (off lambda*)
    N0 = Ns[0]
    ax.loglog(Ns, [3e-2 * (N0 / n) ** 2 for n in Ns], "k:", lw=1.4, alpha=0.8, zorder=5, label=r"$N^{-2}$ (generic)")
    ax.loglog(Ns, [3e-2 * (N0 / n) ** 4 for n in Ns], "k--", lw=1.3, alpha=0.85, zorder=5, label=r"$N^{-4}$ (at $\lambda^\star$)")
    ax.plot([], [], "-", color=cmap[s2vals[len(s2vals) // 2]], lw=2, label=r"36 configs at $\lambda^\star$")
    ax.plot([], [], "-", color="0.6", lw=1.5, label=r"36 configs at $0.7\lambda^\star$")
    sm = plt.cm.ScalarMappable(cmap="viridis", norm=plt.Normalize(min(s2vals), max(s2vals)))
    cb = fig.colorbar(sm, ax=ax, pad=0.02); cb.set_label(r"data variance $s^2$")
    ax.set_xlabel(r"sampler steps $N$"); ax.set_ylabel(r"terminal KL")
    ax.set_title(r"the order jump is universal: every $\lambda^\star$ curve plunges as $N^{-4}$")
    ax.set_ylim(floor, 2.0); ax.legend(fontsize=8.5, loc="lower left", framealpha=0.9)
    ax.grid(True, which="both", alpha=0.15)
    plt.tight_layout(); plt.savefig(os.path.join(FIG, "fig_order_grid_curves.png")); plt.close()
    io.log("fig_order_grid_curves.png", "figs.log")


def fig_floor_order():
    """E31: with an exactly-controllable linear score error delta, the N^-4 descent at lambda*
    truncates at a floor ~ delta^2; the usable window extends as ~1/delta, recovering the exact
    delta=0 result in the limit."""
    r = L("e31_floor_order")
    if not r: return
    import numpy as np, matplotlib
    matplotlib.use("Agg"); import matplotlib.pyplot as plt
    plt.rcParams.update({"font.size": 11, "figure.dpi": 170, "savefig.dpi": 170,
                         "axes.spines.top": False, "axes.spines.right": False, "savefig.bbox": "tight"})
    Ns = np.array(r["Ns"], float)
    rows = r["rows"]
    fig, (ax, ax2) = plt.subplots(1, 2, figsize=(11, 4.3))
    show = [0.05, 0.01, 0.002, 0.0005, 0.0]
    cmap = plt.cm.viridis(np.linspace(0.15, 0.9, len(show)))
    for c, dv in zip(cmap, show):
        rr = min(rows, key=lambda x: abs(x["delta"] - dv))
        kl = np.array(rr["KL"], float)
        lab = r"$\delta=%g$" % rr["delta"] if rr["delta"] > 0 else r"$\delta=0$ (exact)"
        ax.loglog(Ns, kl, "o-", ms=3, lw=1.5, color=c, label=lab)
    g = 1.0 * (Ns[0] / Ns) ** 4 * float(rows[-1]["KL"][0])  # N^-4 guide through first exact point
    ax.loglog(Ns, g, "k--", lw=1.0, alpha=0.6, label=r"$N^{-4}$ guide")
    ax.set_xlabel(r"sampler steps $N$"); ax.set_ylabel(r"terminal KL at $\lambda^\star$")
    ax.set_title(r"the $N^{-4}$ window widens as the score improves")
    ax.legend(fontsize=8, loc="lower left"); ax.grid(True, which="both", alpha=0.2)
    # right: floor ~ delta^2 and window ~ 1/delta
    ds = np.array([x["delta"] for x in rows if x["delta"] > 0], float)
    fl = np.array([x["floor_KL"] for x in rows if x["delta"] > 0], float)
    nw = np.array([x["N_window"] for x in rows if x["delta"] > 0 and x["N_window"] > 0], float)
    dw = np.array([x["delta"] for x in rows if x["delta"] > 0 and x["N_window"] > 0], float)
    ax2.loglog(ds, fl, "s-", color="#d73027", ms=5, label=r"floor (KL)")
    ax2.loglog(ds, fl[0] * (ds / ds[0]) ** 2, "--", color="#d73027", alpha=0.5, lw=1, label=r"$\propto\delta^2$")
    ax2.loglog(dw, nw, "^-", color="#4575b4", ms=5, label=r"window $N^\star$")
    ax2.loglog(dw, nw[0] * (dw[0] / dw), ":", color="#4575b4", alpha=0.6, lw=1, label=r"$\propto\delta^{-1}$")
    ax2.set_xlabel(r"score error $\delta$"); ax2.set_ylabel(r"floor KL  /  window $N^\star$")
    ax2.set_title(r"floor $\propto\delta^2$,\ \ window $\propto\delta^{-1}$")
    ax2.legend(fontsize=8); ax2.grid(True, which="both", alpha=0.2)
    plt.tight_layout(); plt.savefig(os.path.join(FIG, "fig_floor_order.png")); plt.close()
    io.log("fig_floor_order.png", "figs.log")


